import { Type } from 'class-transformer';
import {
  IsEnum,
  IsInt,
  IsOptional,
  IsString,
  Max,
  Min,
} from 'class-validator';

import { SalesOrderSyncStatus } from '../../sales-orders/entities/sales-order.entity';

export class MobileSalesOrderQueryDto {
  @IsOptional()
  @IsEnum(SalesOrderSyncStatus)
  syncStatus?: SalesOrderSyncStatus;

  @IsOptional()
  @IsString()
  search?: string;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  page = 1;

  @IsOptional()
  @Type(() => Number)
  @IsInt()
  @Min(1)
  @Max(100)
  limit = 20;
}
